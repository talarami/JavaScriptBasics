

    // if else - instrukcja warunkowa dodatkowo z blokiem kodu, który będzie wywołany
    // jeśli warunek nie jest spełniony

    let num = 8

    if (num == 10) {
        console.log("num jest równe 10")
    } else {
        console.log("num nie jest równe 10")
    }

    
    // if else if - pozwala na sprawdzenie kolejnych przypadków, jeżeli dany warunek nie jest
    // spełniony to sprawdzany jest kolejny, natomiast gdy już jest spełniony to Java Script
    // wychodzi z konstrukcji if else if i kontynuuje wykonywanie programu.

    let favColor = "red"

    if (favColor == "blue") {
        console.log("blue")
    } else if (favColor == "orange") {
        console.log("orange")
    } else if (favColor == "white") {
        console.log("white")
    } else {
        console.log("default color")
    }



